if (location.hostname === 'pagetual.hoothin.com') {
    window.addEventListener('pagetual_pushState', e => {
        setTimeout(() => {
            window.history.replaceState(undefined, document.title, location.href.replace("/PagetualGuide/", "/"));
        },1);
        setTimeout(() => {
            window.history.replaceState(undefined, document.title, location.href.replace("/PagetualGuide/", "/"));
        },500);
    });
}
document.addEventListener('mousedown',function(e) {
    if (e.target.tagName == "A" && e.target.parentNode && e.target.parentNode.className == "action") {
        e.target.target = '_self';
    }
});